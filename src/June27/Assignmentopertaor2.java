package June27;

public class Assignmentopertaor2 {
    public static void main(String[] args){
        int x =10;
        int y =20;
        System.out.println(x==y);
        System.out.println(x < y);
        System.out.println(x > y);
        System.out.println(x <= y);
        System.out.println(x != y); // ! = not equal to
        x = y; // assignment operator
        System.out.println(x==y);
        System.out.println(x <= y);
        System.out.println(x != y);
        // x == y; it checks the equality
    }
}
